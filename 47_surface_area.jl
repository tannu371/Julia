md"""#### Examples
"""

# ╔═╡ 769d81e2-70e2-11ec-357c-81f5863fa999
md"""Lets see that the surface area of an open cone follows from this formula, even though we just saw how to get this value.
"""

# ╔═╡ 769d8228-70e2-11ec-3651-17ad612af9cc
md"""A cone be be envisioned as rotating the function $f(x) = x\tan(\theta)$ between $0$ and $h$ around the $x$ axis. This integral yields the surface area:
"""

# ╔═╡ 769d8246-70e2-11ec-3690-9d09b1c4a333
md"""```math
\int_0^h 2\pi f(x) \sqrt{1 + f'(x)^2}dx
= \int_0^h 2\pi x \tan(\theta) \sqrt{1 + \tan(\theta)^2}dx
= (2\pi\tan(\theta)\sqrt{1 + \tan(\theta)^2} x^2/2 \big|_0^h
= \pi \tan(\theta) \sec(\theta) h^2
= \pi r^2 / \sin(\theta).
```
"""

# ╔═╡ 769d828c-70e2-11ec-3d33-43bb39c2124b
md"""(There are many ways to express this, we used $r$ and $\theta$ to match the work above. If the cone is parameterized by a height $h$ and radius $r$, then the surface area of the sides is $\pi r\sqrt{h^2 + r^2}$. If the base is included, there is an additional $\pi r^2$ term.)
"""

# ╔═╡ 769d82be-70e2-11ec-2f9b-5f0bb7a46bd9
md"""##### Example
"""

# ╔═╡ 769d82e6-70e2-11ec-0d35-0fc3aa328a40
md"""Let the graph of $f(x) = x^2$ from $x=0$ to $x=1$ be rotated around the $x$ axis. What is the resulting surface area generated?
"""

# ╔═╡ 769d82fa-70e2-11ec-08fc-719978793da2
md"""```math
\text{SA} = \int_a^b 2\pi f(x) \sqrt{1 + f'(x)^2}dx = \int_0^1 2\pi x^2 \sqrt{1 + (2x)^2} dx.
```
"""

# ╔═╡ 769d8336-70e2-11ec-08a5-e51dd324d8f0
md"""This integral is done by a trig substitution, but gets involved. We let `SymPy` do it:
"""

# ╔═╡ 769d8776-70e2-11ec-03c7-0d99a4deff5c
begin
	@syms x
	F = integrate(2 * PI * x^2 * sqrt(1 + (2x)^2), x)
end

# ╔═╡ 769d87b4-70e2-11ec-0d5f-93222b2fed73
md"""We show `F`, only to demonstrate that indeed the integral is a bit involved. The actual surface area follows from a *definite* integral, which we get through the fundamental theorem of calculus:
"""

# ╔═╡ 769d8ac0-70e2-11ec-17d6-4bf94900f334
F(1) - F(0)

# ╔═╡ 769d9c2a-70e2-11ec-356c-21bc4b8ce71b
md"""### Plotting surfaces of revolution
"""

# ╔═╡ 769d9c9c-70e2-11ec-3ff5-cf17efba721b
md"""The commands to plot a surface of revolution will be described more clearly later; for now we present them as simply a pattern to be followed in case plots are desired. Suppose the curve in the $x-y$ plane is given parametrically by $(g(u), f(u))$ for $a \leq u \leq b$.
"""

# ╔═╡ 769d9cb8-70e2-11ec-392a-55e68bb1f389
md"""To be concrete, we parameterize the circle centered at $(6,0)$ with radius $2$ by:
"""

# ╔═╡ 769db3f6-70e2-11ec-2cc5-7b7ba6b22a7a
begin
	g(u) = 6 + 2sin(u)
	f(u) = 2cos(u)
	a, b = 0, 2pi
end

# ╔═╡ 769db420-70e2-11ec-3e56-2935d77aaf37
md"""The plot of this curve is:
"""

# ╔═╡ 769db9ee-70e2-11ec-20d2-0bb6b74f6662
let
	us = range(a, b, length=100)
	plot(g.(us), f.(us), xlims=(-0.5, 9), aspect_ratio=:equal, legend=false)
	plot!([0,0],[-3,3], color=:red, linewidth=5)    # y axis emphasis
	plot!([3,9], [0,0], color=:green, linewidth=5)  # x axis emphasis
end

# ╔═╡ 769dba4a-70e2-11ec-3354-0514235011f1
md"""Though parametric plots have a convenience constructor, `plot(g, f, a, b)`, we constructed the points with `Julia`'s broadcasting notation, as we will need to do  for a surface of revolution. The `xlims` are adjusted to show the $y$ axis, which is emphasized with a layered line. The line is drawn by specifying two points, $(x_0, y_0)$ and $(x_1, y_1)$ in the form `[x0,x1]` and `[y0,y1]`.
"""

# ╔═╡ 769dba68-70e2-11ec-2d33-afa9c29b2741
md"""Now, to rotate this about the $y$ axis, creating a surface plot, we have the following pattern:
"""

# ╔═╡ 769dc2a6-70e2-11ec-2f64-4fb0e1f6aa95
begin
	S(u,v) = [g(u)*cos(v), g(u)*sin(v), f(u)]
	us = range(a, b, length=100)
	vs = range(0, 2pi, length=100)
	ws = unzip(S.(us, vs'))   # reorganize data
	surface(ws..., zlims=(-6,6), legend=false)
	
	plot!([0,0], [0,0], [-3,3], color=:red, linewidth=5) # y axis emphasis
end

# ╔═╡ 769dc2f6-70e2-11ec-2969-43ed80b33d28
md"""The `unzip` function is not part of base `Julia`, rather part of `CalculusWithJulia`. This function rearranges data into a form consumable by the plotting methods like `surface`. In this case, the result of `S.(us,vs')` is a grid (matrix) of points, the result of `unzip` is three grids of values, one for the $x$ values, one for the $y$ values, and one for the $z$ values.  A manual adjustment to the `zlims` is used, as `aspect_ratio` does not have an effect with the `plotly()` backend and errors on 3d graphics with `pyplot()`.
"""

# ╔═╡ 769dc30a-70e2-11ec-2386-abb04b61b373
md"""To rotate this about the $x$ axis, we have this pattern:
"""

# ╔═╡ 76a0cf32-70e2-11ec-1031-1d0fac2e90d9
let
	S(u,v) = [g(u), f(u)*cos(v), f(u)*sin(v)]
	us = range(a, b, length=100)
	vs = range(0, 2pi, length=100)
	ws = unzip(S.(us,vs'))
	surface(ws..., legend=false)
	
	plot!([3,9], [0,0],[0,0], color=:green, linewidth=5) # x axis emphasis
end

# ╔═╡ 76a0cfe6-70e2-11ec-34ae-89be0b77db56
md"""The above pattern covers the case of  rotating the graph of a function $f(x)$ of $a,b$ by taking $g(t)=t$.
"""

# ╔═╡ 76a0d00e-70e2-11ec-13f3-d32e2c7c8e12
md"""##### Example
"""

# ╔═╡ 76a0d040-70e2-11ec-0689-836d4701fc24
md"""Rotate the graph of $x^x$ from $0$ to $3/2$ around the $x$ axis. What is the surface area generated?
"""

# ╔═╡ 76a0d066-70e2-11ec-270b-09577e478d4c
md"""We work numerically for this one, as no antiderivative is forthcoming. Recall, the accompanying `CalculusWithJulia` package defines `f'` to return the automatic derivative through the `ForwardDiff` package.
"""

# ╔═╡ 76a0d522-70e2-11ec-2638-019479094f3f
let
	f(x) = x^x
	a, b = 0, 3/2
	val, _ = quadgk(x -> 2pi * f(x) * sqrt(1 + f'(x)^2), a, b)
	val
end

# ╔═╡ 76a0d590-70e2-11ec-334f-9316d4d4cea9
md"""(The function is not defined at $x=0$ mathematically, but is on the computer to be $1$, the limiting value. Even were this not the case, the `quadgk` function doesn't evaluate the function at the points `a` and `b` that are specified.)
"""

# ╔═╡ 76a0d98c-70e2-11ec-231f-211f7a758843
let
	g(u) = u
	f(u) = u^u
	S(u,v) = [g(u)*cos(v), g(u)*sin(v), f(u)]
	us = range(0, 3/2, length=100)
	vs = range(0, pi, length=100)  # not 2pi (to see inside)
	ws = unzip(S.(us,vs'))
	surface(ws..., alpha=0.75)
end

# ╔═╡ 76a0d9c8-70e2-11ec-0019-6f7949782699
md"""We compare this answer to that of the frustum of a cone with radii $1$ and $(3/2)^2$, formed by rotating the line segment connecting $(0,f(0))$ with $(3/2,f(3/2))$. From looking at the graph of the surface, these values should be comparable. The surface area of the cone part is $\pi (r_1^2 + r_0^2) / \sin(\theta) = \pi (r_1 + r_0) \cdot \sqrt{(\Delta h)^2 + (r_1-r_0)^2}$.
"""

# ╔═╡ 76a0dda6-70e2-11ec-300a-e7350c1016a7
let
	f(x) = x^x
	r0, r1 = f(0), f(3/2)
	pi * (r1 + r0) * sqrt((3/2)^2 + (r1-r0)^2)
end

# ╔═╡ 76a0ddba-70e2-11ec-383d-bfb1b785d6ce
md"""##### Example
"""

# ╔═╡ 76a0dde0-70e2-11ec-3e46-a9387a61b395
md"""What is the surface area generated by Gabriel's Horn, the solid formed by rotating $1/x$ for $x \geq 1$ around the $x$ axis?
"""

# ╔═╡ 76a0de12-70e2-11ec-0c01-cdca345d2012
md"""```math
\text{SA} = \int_a^b 2\pi f(x) \sqrt{1 + f'(x)^2}dx =
\lim_{M \rightarrow \infty} \int_1^M 2\pi \frac{1}{x} \sqrt{1 + (-1/x^2)^2} dx.
```
"""

# ╔═╡ 76a0de32-70e2-11ec-3ce7-27d4f515000f
md"""We do this with `SymPy`:
"""

# ╔═╡ 76a0e1e6-70e2-11ec-1d1c-c5c4798102cb
begin
	@syms M
	ex = integrate(2PI * (1/x) * sqrt(1 + (-1/x)^2), (x, 1, M))
end

# ╔═╡ 76a0e210-70e2-11ec-193d-0358f92a4619
md"""The limit as $M$ gets large is of interest. The only term that might get out of hand is `asinh(M)`.  We check its limit:
"""

# ╔═╡ 76a0e6de-70e2-11ec-16d9-171db815a152
limit(asinh(M), M => oo)

# ╔═╡ 76a0e706-70e2-11ec-1618-ffaa54aeed30
md"""So indeed it does. There is nothing to balance this out, so the integral will be infinite, as this shows:
"""

# ╔═╡ 76a0ea08-70e2-11ec-3067-d95b72e402e3
limit(ex, M => oo)

# ╔═╡ 76a0ea56-70e2-11ec-1f6e-57f1cf57f245
md"""This figure would have infinite surface, were it possible to actually construct an infinitely long solid. (But it has been shown to have *finite* volume.)
"""

# ╔═╡ 76a0ea76-70e2-11ec-209b-ffc1f4c80a88
md"""##### Example
"""

# ╔═╡ 76a0eac8-70e2-11ec-31ce-5133e49af4b5
md"""The curve described parametrically by $g(t) = 2(1 + \cos(t))\cos(t)$ and $f(t) = 2(1 + \cos(t))\sin(t)$ from $0$ to $\pi$ is rotated about the $x$ axis. Find the resulting surface area.
"""

# ╔═╡ 76a0eada-70e2-11ec-154f-fdc52360365d
md"""The graph shows half a heart, the resulting area will resemble an apple.
"""

# ╔═╡ 76a0f41c-70e2-11ec-210a-9f1431d17659
let
	g(t) = 2(1 + cos(t)) * cos(t)
	f(t) = 2(1 + cos(t)) * sin(t)
	plot(g, f, 0, 1pi)
end

# ╔═╡ 76a0f458-70e2-11ec-1dbe-0fafbec8f0b7
md"""The integrand simplifies to $8\sqrt{2}\pi \sin(t) (1 + \cos(t))^{3/2}$. This lends itself to $u$-substitution with $u=\cos(t)$.
"""

# ╔═╡ 76a0f476-70e2-11ec-3837-db9b27895605
md"""```math
\begin{align*}
\int_0^\pi 8\sqrt{2}\pi \sin(t) (1 + \cos(t))^{3/2}
&= 8\sqrt{2}\pi \int_1^{-1} (1 + u)^{3/2} (-1) du\\
&= 8\sqrt{2}\pi (2/5) (1+u)^{5/2} \big|_{-1}^1\\
&= 8\sqrt{2}\pi (2/5) 2^{5/2} = \frac{2^7 \pi}{5}.
\end{align*}
```
"""

# ╔═╡ 76a0f4bc-70e2-11ec-11ac-396e47ea0096
md"""## The first Theorem of Pappus
"""

# ╔═╡ 76a2af50-70e2-11ec-3394-a7eb419a71e8
md"""The [first](http://tinyurl.com/le3lvb9) theorem of Pappus provides a simpler means to compute the surface area if the distance the centroid is from the axis ($\rho$) and the arc length of the curve ($L$) are both known. In that case, the surface area satisfies:
"""

# ╔═╡ 76a2afaa-70e2-11ec-3d1c-13dd7eb22d5d
md"""```math
\text{SA} = 2 \pi \rho L
```
"""

# ╔═╡ 76a2afd2-70e2-11ec-0420-69b54cb95187
md"""That is, the surface area is simply the circumference of the circle traced out by the centroid of the curve times the length of the curve - the distances rotated are collapsed to that of just the centroid.
"""

# ╔═╡ 76a2b004-70e2-11ec-0d69-3f0c380d6f32
md"""##### Example
"""

# ╔═╡ 76a2b02c-70e2-11ec-065e-7fc12d93dcf3
md"""The surface area of of an open cone can be computed, as the arc length is $\sqrt{h^2 + r^2}$ and the centroid of the line is a distance $r/2$ from the axis. This gives SA$=2\pi (r/2) \sqrt{h^2 + r^2} = \pi r \sqrt{h^2 + r^2}$.
"""

# ╔═╡ 76a2b040-70e2-11ec-382d-e76a4bdb3ed0
md"""##### Example
"""

# ╔═╡ 76a2b048-70e2-11ec-014b-eba832b8c652
md"""We can get the surface area of a torus from this formula.
"""

# ╔═╡ 76a2b072-70e2-11ec-2fdc-f76d2195d57b
md"""The torus is found by rotating the curve $(x-b)^2 + y^2 = a^2$ about the $y$ axis. The centroid is $b$, the arc length $2\pi a$, so the surface area is $2\pi (b) (2\pi a) = 4\pi^2 a b$.
"""

# ╔═╡ 76a2b086-70e2-11ec-2327-399e99ccfcf9
md"""A torus with $a=2$ and $b=6$
"""

# ╔═╡ 76a2b87e-70e2-11ec-2598-3365ad3464ea
let
	a,b = 2, 6
	F₀(u,v) = [a*(cos(u) + b)*cos(v), a*(cos(u) + b)*sin(v), a*sin(u)]
	us = vs = range(0, 2pi, length=35)
	ws = unzip(F₀.(us, vs'))
	surface(ws..., legend=false, zlims=(-12,12))
end

# ╔═╡ 76a2b89c-70e2-11ec-08de-6963c8773f85
md"""##### Example
"""

# ╔═╡ 76a2b8e2-70e2-11ec-0f49-f967d65885d9
md"""The surface area of sphere will be SA$=2\pi \rho (\pi r) = 2 \pi^2 r \cdot \rho$. What is $\rho$? The centroid of an arc formula can be derived in a manner similar to that of the centroid of a region. The formulas are:
"""

# ╔═╡ 76a2b90a-70e2-11ec-3516-fdbbcb3a6af6
md"""```math
\begin{align}
\text{cm}_x &= \frac{1}{L} \int_a^b g(t) \sqrt{g'(t)^2 + f'(t)^2} dt\\
\text{cm}_y &= \frac{1}{L} \int_a^b f(t) \sqrt{g'(t)^2 + f'(t)^2} dt.
\end{align}
```
"""

# ╔═╡ 76a2b91e-70e2-11ec-2004-4bb1107c89a7
md"""Here, $L$ is the arc length of the curve.
"""

# ╔═╡ 76a2b946-70e2-11ec-33d1-4dd8e7e450dd
md"""For the sphere parameterized by $g(t) = r \cos(t)$, $f(t) = r\sin(t)$, we get that these become
"""

# ╔═╡ 76a2b95c-70e2-11ec-1d9a-c5706d6b2de1
md"""```math
\text{cm}_x = \frac{1}{L}\int_0^\pi r\cos(t) \sqrt{r^2(\sin(t)^2 + \cos(t)^2)} dt =  \frac{1}{L}r^2 \int_0^\pi \cos(t) = 0.
```
"""

# ╔═╡ 76a2b96e-70e2-11ec-2bbb-eb998a78288b
md"""```math
\text{cm}_y =  \frac{1}{L}\int_0^\pi r\sin(t) \sqrt{r^2(\sin(t)^2 + \cos(t)^2)} dt =  \frac{1}{L}r^2 \int_0^\pi \sin(t) = \frac{1}{\pi r} r^2 \cdot 2 = \frac{2r}{\pi}.
```
"""

# ╔═╡ 76a2b982-70e2-11ec-1f12-79f1fc13f497
md"""Combining this, we see that the surface area of a sphere is $2 \pi^2 r (2r/\pi) = 4\pi r^2$, by Pappus' Theorem.
"""

