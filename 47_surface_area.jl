# ╔═╡ 769d34ee-70e2-11ec-06d5-53dd08e355f8
md"""> The surface area generated by rotating the graph of $f(x)$ between $a$ and $b$  about the $x$-axis is given by the integral
>
> ```math
> \int_a^b 2\pi f(x) \cdot \sqrt{1 + f'(x)^2} dx.
> ```
>
> If the curve is parameterized by $(g(t), f(t))$ between $a$ and $b$ then the surface area is
>
> ```math
> \int_a^b 2\pi f(t) \cdot \sqrt{g'(t)^2 + f'(t)^2} dx.
> ```
>
> These formulas do not add in the surface area of either of the ends.

"""

# ╔═╡ 769d41be-70e2-11ec-2ff8-8fd2ec85edae
let
	F₀(u,v) = [u, u*cos(v), u*sin(v)] # a cone
	us = range(0, 1, length=25)
	vs = range(0, 2pi, length=25)
	ws = unzip(F₀.(us, vs')) # make square
	surface(ws..., legend=false)
	plot!([-0.5,1.5], [0,0],[0,0])
end

# ╔═╡ 769d424a-70e2-11ec-3cff-05c24881eaa5
md"""The above figure shows a cone (the line $y=x$) presented as a surface of revolution about the $x$-axis.
"""

# ╔═╡ 769d4272-70e2-11ec-3618-5947178cac4a
md"""To see why this formula is as it is, we look at the parameterized case, the first one being a special instance with $g(t) =t$.
"""

# ╔═╡ 769d42a4-70e2-11ec-19c8-230cc5b81437
md"""Let a partition of $[a,b]$ be given by $a = t_0 < t_1 < t_2 < \cdots < t_n =b$. This breaks the curve into a collection of line segments. Consider the line segment connecting $(g(t_{i-1}), f(t_{i-1}))$ to $(g(t_i), f(t_i))$. Rotating this around the $x$ axis will generate something approximating a disc, but in reality will be the frustum of a cone. What will be the surface area?
"""

# ╔═╡ 769d4308-70e2-11ec-2b3c-8fb807fe87b7
md"""Consider a right-circular cone parameterized by an angle $\theta$ and the largest radius $r$ (so that the height satisfies $r/h=\tan(\theta)$). If this cone were made of paper, cut up a side, and layed out flat, it would form a sector of a circle, whose area would be $R\gamma$ where $R$ is the radius of the circle (also the side length of our cone), and $\gamma$ an angle that we can figure out from $r$ and $\theta$. To do this, we note that the arc length of the circle's edge is $R\gamma$ and also the circumference of the bottom of the cone so $R\gamma = 2\pi r$. With all this, we can solve to get $A = \pi r^2/\sin(\theta)$. But we have a frustum of a cone with radii $r_0$ and $r_1$, so the surface area is a difference: $A = \pi (r_1^2 - r_0^2) /\sin(\theta)$.
"""

# ╔═╡ 769d436c-70e2-11ec-15e5-fb1a914d7c2d
md"""Relating this to our values in terms of $f$ and $g$, we have $r_1=f(t_i)$, $r_0 = f(t_{i-1})$, and $\sin(\theta) = \Delta f / \sqrt{(\Delta g)^2 + (\Delta f)^2}$, where $\Delta f = f(t_i) - f(t_{i-1})$ and similarly for $\Delta g$.
"""

# ╔═╡ 769d4394-70e2-11ec-02ef-7b1910ec9df3
md"""Putting this altogether we get that the surface area generarated by rotating the line segment around the $x$ axis is
"""

# ╔═╡ 769d43da-70e2-11ec-1f43-b7b585d965e2
md"""```math
\text{sa}_i = \pi (f(t_i)^2 - f(t_{i-1})^2) \cdot \sqrt{(\Delta g)^2 + (\Delta f)^2} / \Delta f =
\pi (f(t_i) + f(t_{i-1})) \cdot \sqrt{(\Delta g)^2 + (\Delta f)^2}.
```
"""

# ╔═╡ 769d4402-70e2-11ec-07b5-d78859785e60
md"""(This is $2 \pi$ times the average radius times the slant height.)
"""

# ╔═╡ 769d4434-70e2-11ec-316f-53e5b32d7c44
md"""As was done in the derivation of the formula for arc length, these pieces are multiplied both top and bottom by $\Delta t = t_{i} - t_{i-1}$. Carrying the bottom inside the square root and noting that by the mean value theorem $\Delta g/\Delta t = g(\xi)$ and $\Delta f/\Delta t = f(\psi)$ for some $\xi$ and $\psi$ in $[t_{i-1}, t_i]$, this becomes:
"""

# ╔═╡ 769d4448-70e2-11ec-3541-c1d40a8051bd
md"""```math
\text{sa}_i = \pi (f(t_i) + f(t_{i-1})) \cdot \sqrt{(g'(\xi))^2 + (f'(\psi))^2} \cdot (t_i - t_{i-1}).
```
"""

# ╔═╡ 769d4466-70e2-11ec-1e58-4b1fa704c1d6
md"""Adding these up, $\text{sa}_1 + \text{sa}_2 + \cdots + \text{sa}_n$, we get a Riemann sum approximation to the integral
"""

# ╔═╡ 769d447a-70e2-11ec-1c8d-9d42dd59e1b7
md"""```math
\text{SA} = \int_a^b 2\pi f(t) \sqrt{g'(t)^2 + f'(t)^2} dt.
```
"""

# ╔═╡ 769d44ac-70e2-11ec-021d-e3433ad95a92
md"""If we assume integrability of the integrand, then as our partition size goes to zero, this approximate surface area converges to the value given by the limit.  (As with arc length, this needs a technical adjustment to the Riemann integral theorem as here we are evaluating the integrand function at four points ($t_i$, $t_{i-1}$, $\xi$ and $\psi$) and not just at some $c_i$.
"""

# ╔═╡ 769d8160-70e2-11ec-161e-3958943d3b2e
let
	## {{{approximate_surface_area}}}
	
	xs,ys = range(-1, stop=1, length=50), range(-1, stop=1, length=50)
	f(x,y)= 2 - (x^2 + y^2)
	
	dr = [1/2, 3/4]
	df = [f(dr[1],0), f(dr[2],0)]
	
	function sa_approx_graph(i)
	    p = plot(xs, ys, f, st=[:surface], legend=false)
	    for theta in range(0, stop=i/10*2pi, length=10*i )
	        path3d!(p,sin(theta)*dr, cos(theta)*dr, df)
	    end
	    p
	end
	n = 10
	
	anim = @animate for i=1:n
	    sa_approx_graph(i)
	end
	
	imgfile = tempname() * ".gif"
	gif(anim, imgfile, fps = 1)
	
	
	caption = L"""
	
	Surface of revolution of $f(x) = 2 - x^2$ about the $y$ axis. The lines segments are the images of rotating the secant line connecting $(1/2, f(1/2))$ and $(3/4, f(3/4))$. These trace out the frustum of a cone which approximates the corresponding surface area of the surface of revolution. In the limit, this approximation becomes exact and a formula for the surface area of surfaces of revolution can be used to compute the value.
	
	"""
	
	ImageFile(imgfile, caption)
end

