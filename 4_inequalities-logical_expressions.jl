md"""## Algebra of inequalities
"""

# ╔═╡ 16b1ebfe-53c0-11ec-1e49-85bbe1375432
md"""To illustrate, let's see that the algebra of expressions works as expected.
"""

# ╔═╡ 16b1ed02-53c0-11ec-1e9c-db0fad8b3ccf
md"""For example, if $a < b$ then for any $c$ it is also true that $a + c < b + c$.
"""

# ╔═╡ 16b1eddc-53c0-11ec-0200-3747fa27aef7
md"""We can't "prove" this through examples, but we can investigate it by the choice of various values of $a$, $b$, and $c$. For example:
"""

# ╔═╡ 16b2c132-53c0-11ec-23df-2ded88db8924
let
	a,b,c = 1,2,3
	a < b, a + c < b + c
end

# ╔═╡ 16b2c29a-53c0-11ec-2306-c7797b538c6a
md"""Or in reverse:
"""

# ╔═╡ 16b33644-53c0-11ec-14d3-8963e015c5c9
let
	a,b,c = 3,2,1
	a < b, a + c < b + c
end

# ╔═╡ 16b336d0-53c0-11ec-37e9-51475119e239
md"""Trying other choices will show that the two answers are either both `false` or both `true`.
"""

# ╔═╡ 16b3d554-53c0-11ec-2edf-577ee8153bdd
alert(""" Well, almost... When `Inf` or `NaN` are involved, this may not hold, for example `1 + Inf < 2 + Inf` is actually `false`. As would be `1 + (typemax(1)-1) < 2 + (typemax(1)-1)`.""")

# ╔═╡ 16b3d694-53c0-11ec-03c9-817787e93f5a
md"""So adding or subtracting most any finite value from an inequality will preserve the inequality, just as it does for equations.
"""

# ╔═╡ 16b3d6d2-53c0-11ec-0ba6-dfcaf8cdba76
md"""What about addition and multiplication?
"""

# ╔═╡ 16b40600-53c0-11ec-04d4-c9737b321004
md"""Consider the case $a < b$ and $c > 0$. Then $ca < cb$. Here we investigate using 3 random values (which will be positive):
"""

# ╔═╡ 16b42430-53c0-11ec-010e-3be296410095
let
	a,b,c = rand(3)   # 3 random numbers in [0,1)
	a < b, c*a < c*b
end

# ╔═╡ 16b424be-53c0-11ec-3b61-dd69b2699f15
md"""Whenever these two commands are run, the two logical values should be identical, even though the specific values of `a`, `b`, and `c` will vary.
"""

# ╔═╡ 16b425e0-53c0-11ec-0f4d-db45b620abc9
md"""The restriction that $c > 0$ is needed. For example, if $c = -1$, then we have $a < b$ if and only if $-a > -b$. That is the inequality is "flipped."
"""

# ╔═╡ 16b43e20-53c0-11ec-1744-01daacfbabe4
let
	a,b = rand(2)
	a < b, -a > -b
end

# ╔═╡ 16b43f44-53c0-11ec-2b70-0fbc6926fa69
md"""Again, whenever this is run, the two logical values should be the same. The values $a$ and $-a$ are the same distance from $0$, but on opposite sides. Hence if $0 < a < b$, then $b$ is farther from $0$ than $a$, so $-b$ will be farther from $0$ than $-a$, which in this case says $-b < -a$, as expected.
"""

# ╔═╡ 16b43fba-53c0-11ec-15d1-adc988e201bf
md"""Finally, we have the case of division. The relation of $x$ and $1/x$ (for $x > 0$) is that the farther $x$ is from $0$, the closer $1/x$ is to $0$. So large values of $x$ make small values of $1/x$. This leads to this fact for $a,b > 0$: $a < b$ if and only if $1/a > 1/b$.
"""

# ╔═╡ 16b44052-53c0-11ec-0bf0-5d297a617aa8
md"""We can check with random values again:
"""

# ╔═╡ 16b44746-53c0-11ec-1fdf-092e7aeba1e6
let
	a,b = rand(2)
	a < b, 1/a > 1/b
end

# ╔═╡ 16b4476e-53c0-11ec-1ffb-5d2d87aedbaf
md"""In summary we investigated numerically that the following hold:
"""

# ╔═╡ 16b457fe-53c0-11ec-056f-1d0f1b25676a
md"""  * `a < b` if and only if `a + c < b + c` for all finite `a`, `b`, and `c`.
  * `a < b` if and only if `c*a < c*b` for all finite `a` and `b`, and finite, positive `c`.
  * `a < b` if and only if `-a > -b` for all finite `a` and `b`.
  * `a < b` if and only if `1/a > 1/b` for all finite, positive `a` and `b`.
"""

