let
	x = -1; exp(x) >= 1 + x
	x =  0; exp(x) >= 1 + x
	x =  1; exp(x) >= 1 + x
end

# ╔═╡ 16b4bd70-53c0-11ec-2e54-03c73d5f9761
md"""Now, let's investigate that for any distinct real numbers, $a$ and $b$, that
"""

# ╔═╡ 16b4c108-53c0-11ec-2962-a31a5fa47290
md"""```math
\frac{e^b - e^a}{b - a} > e^{(a+b)/2}
```
"""

# ╔═╡ 16b4c1a8-53c0-11ec-19b2-4599ee730808
md"""For this, we use `rand(2)` to generate two random numbers in $[0,1)$:
"""

# ╔═╡ 16b4cab8-53c0-11ec-1b47-0d97e23c8867
let
	a, b = rand(2)
	(exp(b) - exp(a)) / (b-a) > exp((a+b)/2)
end

# ╔═╡ 16b4cbb2-53c0-11ec-385f-4b813f3dac2f
md"""This should evaluate to `true` for any random choice of `a` and `b` returned by `rand(2)`.
"""

# ╔═╡ 16b4cc52-53c0-11ec-3051-3d2d54d06445
md"""Finally, let's investigate the fact that the harmonic mean, $2/(1/a + 1/b)$ is less than or equal to the geometric mean, $\sqrt{ab}$, which is less than or equal to the quadratic mean, $\sqrt{a^2 + b^2}/\sqrt{2}$, using two randomly chosen values:
"""

# ╔═╡ 16b4eb24-53c0-11ec-27f2-576bb9dcbbbd
let
	a, b = rand(2)
	h = 2 / (1/a + 1/b)
	g = (a * b) ^ (1 / 2)
	q = sqrt((a^2 + b^2) / 2)
	h <= g, g <= q
end

# ╔═╡ 16b4ebec-53c0-11ec-2128-5ba4a40cd7ed
md"""## Chaining, combining expressions: absolute values
"""

# ╔═╡ 16b4ec5a-53c0-11ec-2755-511aac0eddc1
md"""The absolute value notation can be defined through cases:
"""

# ╔═╡ 16b4ec8c-53c0-11ec-2911-3df115a82e09
md"""```math
\lvert x\rvert = \begin{cases}
x & x \geq 0\\
-x & \text{otherwise}.
\end{cases}
```
"""

# ╔═╡ 16b4ece6-53c0-11ec-186e-4b7681eebc8a
md"""The interpretation of $\lvert x\rvert$, as the distance on the number line of $x$ from $0$, means that many relationships are naturally expressed in terms of absolute values. For example, a simple shift: $\lvert x -c\rvert$ is related to the distance $x$ is from the number $c$. As common as they are, the concept can still be confusing when inequalities are involved.
"""

# ╔═╡ 16b515ae-53c0-11ec-2dd8-ff6885c9709b
md"""For example, the expression $\lvert x - 5\rvert < 7$ has solutions which are all values of $x$ within $7$ units of $5$. This would be the values $-2< x < 12$. If this isn't immediately intuited, then formally $\lvert x - 5\rvert <7$ is a compact representation of a chain of inequalities: $-7 < x-5 < 7$. (Which is really two combined inequalities: $-7 < x-5$ *and* $x-5 < 7$.) We can "add" 5 to each side to get $-2 < x < 12$, using the fact that adding by a finite number does not change the inequality sign.
"""

# ╔═╡ 16b51612-53c0-11ec-1763-d5606514ed94
md"""Julia's precedence for logical expressions, allows such statements to mirror the mathematical notation:
"""

# ╔═╡ 16b51d24-53c0-11ec-1b12-5dd6da7d0200
begin
	x = 18
	abs(x - 5) < 7
end

# ╔═╡ 16b51dd8-53c0-11ec-0673-851c948f89de
md"""This is to be expected, but we could also have written:
"""

# ╔═╡ 16b524cc-53c0-11ec-247a-0b9bebf1d337
-7 < x - 5 < 7

# ╔═╡ 16b5261e-53c0-11ec-1c0c-e7ae3b21cd3d
md"""Read aloud this would be "minus 7 is less than x minus 5 **and** x minus 5 is less than 7".
"""

# ╔═╡ 16b527e2-53c0-11ec-2db1-915608f747c9
md"""The "and" equations can be combined as above with a natural notation. However, an equation like $\lvert x - 5\rvert > 7$ would emphasize an **or** and be "x minus 5 less than minus 7 **or** x minus 5 greater than 7". Expressing this requires some new notation.
"""

# ╔═╡ 16b529b8-53c0-11ec-03e8-19ef164fac03
md"""The *boolean shortcut operators* `&&` and `||` implement "and" and "or." (There are also *bitwise* boolean operators `&` and `|`, but we only describe the former.)
"""

# ╔═╡ 16b529e0-53c0-11ec-3807-89c06252e092
md"""Thus we could write $-7 < x-5 < 7$ as
"""

# ╔═╡ 16b57b3e-53c0-11ec-13af-99bad9a90732
(-7 < x - 5) && (x - 5 < 7)

# ╔═╡ 16b57cec-53c0-11ec-14e3-c91ea4f5e173
md"""and could write $\lvert x-5\rvert > 7$ as
"""

# ╔═╡ 16b58714-53c0-11ec-18cc-0755a70e5fd4
(x - 5 < -7) || (x - 5 > 7)

# ╔═╡ 16b58804-53c0-11ec-1fb4-1be5cb49ef4f
md"""(The first expression is false for $x=18$ and the second expression true, so the "or"ed result is `true` and the "and" result if `false`.)
"""

# ╔═╡ 16b58c50-53c0-11ec-2dd4-894944e205ff
md"""##### Example
"""

# ╔═╡ 16b58d72-53c0-11ec-2407-09540dabd319
md"""One of [DeMorgan's Laws](http://en.wikipedia.org/wiki/De_Morgan%27s_laws) states that "not (A and B)" is the same as "(not A) or (not B)". This is a kind of distributive law for "not", but note how the "and" changes to "or". We can verify this law systematically. For example, the following shows it true for 1 of the 4 possible cases for the pair `A`, `B` to take:
"""

# ╔═╡ 16b596a0-53c0-11ec-206b-11d0acd65780
begin
	A,B = true, false  ## also true, true; false, true; and false, false
	!(A && B) == !A || !B
end

# ╔═╡ 16b59740-53c0-11ec-0c28-974b7a6e527e
md"""## Precedence
"""

# ╔═╡ 16b5995c-53c0-11ec-3af2-4902a2e40ec8
md"""The question of when parentheses are needed and when they are not is answered by the [precedence](http://julia.readthedocs.org/en/latest/manual/mathematical-operations/#operator-precedence)  rules implemented. Earlier, we wrote
"""

# ╔═╡ 16b5a296-53c0-11ec-1b8c-b9f6906fdee1
(x - 5 < -7) || (x - 5 > 7)

# ╔═╡ 16b5a348-53c0-11ec-3293-9d110d958530
md"""To represent $\lvert x-5\rvert > 7$. Were the parentheses necessary? Let's just check.
"""

# ╔═╡ 16b5aaaa-53c0-11ec-2c04-6f41f3927e58
x - 5 < -7 || x - 5 > 7

# ╔═╡ 16b5ab4a-53c0-11ec-1eb5-9f2cdef53688
md"""So no, they were not.
"""

# ╔═╡ 16b5acd0-53c0-11ec-2d2a-37262767f9d5
md"""An operator (such as `<`, `>`, `||` above) has an associated associativity and precedence. The associativity is whether an expression like `a - b - c` is `(a-b) - c` or `a - (b-c)`. The former being left associative, the latter right. Of issue here is *precedence*, as in with two or more different operations, which happens first, second, $\dots$.
"""

# ╔═╡ 16b5ad5c-53c0-11ec-190e-cba71689a082
md"""The table in the manual on [operator precedence and associativity](https://docs.julialang.org/en/v1/manual/mathematical-operations/#Operator-Precedence-and-Associativity) shows that for these operations "control flow" (the `&&` above) is lower than "comparisons" (the `<`, `>`), which are lower than "Addition" (the `-` above). So the expression without parentheses would be equivalent to:
"""

# ╔═╡ 16b5d32c-53c0-11ec-1510-db07f8cb1117
((x-5) < -7) && ((x-5) > 7)

# ╔═╡ 16b5d438-53c0-11ec-3125-1fc0873562d7
md"""(This is different than the precedence of the bitwise boolean operators, which have `&` with "Multiplication" and `|` with "Addition", so `x-5 < 7 | x - 5 > 7` would need parentheses.)
"""

# ╔═╡ 16b5d4dc-53c0-11ec-3278-d32c1e7b5aeb
md"""A thorough understanding of the precedence rules can help eliminate unnecessary parentheses, but in most cases it is easier just to put them in.
"""

