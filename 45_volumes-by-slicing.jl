	v₅ = 5
	h5 = find_zero(h -> Vol(h) - v₅ * 231 / 128, 4)
end

# ╔═╡ 5a811702-70d4-11ec-12ae-8dc42b8ef3b0
md"""and
"""

# ╔═╡ 5a811aa4-70d4-11ec-22e3-af5d2576416f
begin
	v₁₂ = 12
	h12 = find_zero(h -> Vol(h) - v₁₂ * 231 / 128, 4)
end

# ╔═╡ 5a811ac2-70d4-11ec-30b5-215cbe9c170c
md"""As a percentage of the total height, these are:
"""

# ╔═╡ 5a811d2e-70d4-11ec-168b-69314f93f6e7
h5/h, h12/h

