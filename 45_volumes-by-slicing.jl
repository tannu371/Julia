# ╔═╡ 5a8a3a12-70d4-11ec-07a4-3396ba09fb93
md"""You can integrate in the length along the line $y=x$ ($u$ from $0$ to $\sqrt{2}$). The radius then can be found by intersecting the line perpendicular line to $y=x$ at $u$ to the curve $f(x)$. This will do so:
