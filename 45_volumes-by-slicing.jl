"""

# ╔═╡ 5a80cdb8-70d4-11ec-1b01-35a16b8bba6d
md"""A back-of-the envelope calculation would start by
"""

# ╔═╡ 5a80cf40-70d4-11ec-01d7-0762bb0598a4
md"""  * Mentally separating out each "tire" and lining them up one by one.
  * Counting the number of "tires" (or rings), say $n$.
  * Estimating the radius for each tire, say $r_i$ for $1 \leq i \leq n$.
  * Estimating the height for each tire, say $h_i$ for $1 \leq i \leq n$
"""

# ╔═╡ 5a80cf54-70d4-11ec-35ab-fb5849b77b1b
md"""Then the volume would be found by adding:
"""

# ╔═╡ 5a80cf7c-70d4-11ec-1eb0-3f4aa986bb27
md"""```math
V = \pi \cdot r_1^2 \cdot h_1 + \pi \cdot r_2^2 \cdot h_2 + \cdot + \pi \cdot r_n^2 \cdot h_n.
```
"""

# ╔═╡ 5a80cf92-70d4-11ec-05a8-fd5eb62c0d48
md"""The weight would come by multiplying the volume by some appropriate density.
"""

# ╔═╡ 5a80cfa4-70d4-11ec-073e-797bd43c824e
md"""Looking at the sum though, we see the makings of an approximate integral. If the heights were to get infinitely small, we might expect this to approach something like $V=\int_a^b \pi r(h)^2  dh$.
"""

# ╔═╡ 5a80cfae-70d4-11ec-3927-4fbfeca3b170
md"""In fact, we have in general:
"""

# ╔═╡ 5a80d09e-70d4-11ec-207c-c77a70a60981
md"""> **Volume of a figure with a known cross section**: The volume of a solid with known cross-sectional area $A_{xc}(x)$ from $x=a$ to $x=b$ is given by
>
> $V = \int_a^b A_{xc}(x) dx.$
>
> This assumes $A_{xc}(x)$ is integrable.

"""

# ╔═╡ 5a80d0d0-70d4-11ec-3566-996e5804d025
md"""This formula is derived by approximating the volume by "slabs" with volume $A_{xc}(x) \Delta x$ and using the Riemann integral's definition to pass to the limit. The discs of the Michelin man are an example, where the cross-sectional area is just that of a circle, or $\pi r^2$.
"""

