The formula is for a rotation around the $x$-axis, but can easily be generalized to rotating around any line (say the $y$-axis or $y=x$, ...) just by adjusting what $r(x)$ is taken to be.

""")

# ╔═╡ 5a80e07a-70d4-11ec-1d77-e53f0779686c
md"""For a numeric example, we consider the original Red [Solo](http://en.wikipedia.org/wiki/Red_Solo_Cup) Cup. The dimensions of the cup were basically: a top diameter of $d_1 = 3~ \frac{3}{4}$ inches, a bottom diameter of $d_0 = 2~ \frac{1}{2}$ inches and a height of $h = 4~ \frac{3}{4}$ inches.
"""

# ╔═╡ 5a80e08e-70d4-11ec-3808-39aac7b8788c
md"""The central axis is straight down. If we rotate the cup so this is the $x$-axis, then we can get
"""

# ╔═╡ 5a80e0a0-70d4-11ec-0024-711a75d53d72
md"""```math
r(x) = \frac{d_0}{2} + \frac{d_1/2 - d_0/2}{h}x = \frac{5}{4} + \frac{5}{38}x
```
"""

# ╔═╡ 5a80e0ac-70d4-11ec-1b1f-91d0cc084457
md"""The volume in cubic inches will be:
"""

# ╔═╡ 5a80e0c0-70d4-11ec-0c99-b37cc7a185e0
md"""```math
V = \int_0^h \pi r(x)^2 dx
```
"""

# ╔═╡ 5a80e0ca-70d4-11ec-1dba-fffa8d43ded8
md"""This is
"""

# ╔═╡ 5a80e822-70d4-11ec-1c48-1be591f4a609
begin
	d0, d1, h = 2.5, 3.75, 4.75
	r(x) = d0/2 + (d1/2 - d0/2)/h * x
	vol, _ = quadgk(x -> pi * r(x)^2, 0, h)
end

# ╔═╡ 5a80e868-70d4-11ec-21de-652ed5da027e
md"""So $36.9 \text{in}^3$. How many ounces is that? It is useful to know that 1 [gallon](http://en.wikipedia.org/wiki/Gallon) of water is defined as $231$ cubic inches, contains $128$ ounces, and weighs $8.34$ pounds.
"""

# ╔═╡ 5a80e872-70d4-11ec-33e4-e151638ffc59
md"""So our cup holds this many ounces:
"""

# ╔═╡ 5a810208-70d4-11ec-13c1-357f853c6f2e
ozs = vol / 231 * 128

# ╔═╡ 5a810262-70d4-11ec-38b7-f56dce178218
md"""Full it is about $20$ ounces, though this doesn't really account for the volume taken up by the bottom of the cup, etc.
