md"""What is the height for $5$ ounces (for a glass of wine)? $12$ ounces (for a beer unit)?
"""

# ╔═╡ 5a810c4e-70d4-11ec-3af1-5bd0713bcbf0
md"""Here the volume is fixed, but the height is not. For $v$ ounces, we need to convert to cubic inches. The conversion is $1$ ounce is $231/128 \text{in}^3$.
"""

# ╔═╡ 5a810c6c-70d4-11ec-3e29-691f0683f783
md"""So we need to solve $v \cdot (231/128) = \int_0^h\pi r(x)^2 dx$ for $h$ when $v=5$ and $v=12$.
"""

# ╔═╡ 5a810c80-70d4-11ec-2d13-03e7d847ebc6
md"""Let's express volume as a function of $h$:
"""

# ╔═╡ 5a8112fc-70d4-11ec-0f99-dfc491446a32
Vol(h) = quadgk(x -> pi * r(x)^2, 0, h)[1]

# ╔═╡ 5a811310-70d4-11ec-3f91-2549c9d0adee
md"""Then to solve we have:
"""

# ╔═╡ 5a8116f0-70d4-11ec-187a-f5f7f42597f1
