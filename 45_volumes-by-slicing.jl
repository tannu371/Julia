""")

# ╔═╡ 5a813ea8-70d4-11ec-373f-8362e1b2a7cb
md"""##### Example
"""

# ╔═╡ 5a813f02-70d4-11ec-36e3-efe81fba63a8
md"""By rotating the line segment $x/r + y/h=1$ that sits in the first quadrant around the $y$ axis, we will generate a right-circular cone. The volume of which can be expressed through the above formula by noting the radius, as a function of $y$, will be $R = r(1 - y/h)$. This gives the well-known volume of a cone:
"""

# ╔═╡ 5a814434-70d4-11ec-3e80-89cdd5a87d4a
let
	@syms r h x y
	R = r*(1 - y/h)
	integrate(pi*R^2, (y, 0, h))
end

# ╔═╡ 5a814466-70d4-11ec-06aa-cd2a20daaf12
md"""The frustum of a cone is simply viewed as a cone with its top cut off. If the original height would have been $h_0$ and the actual height $h_1$, then the volume remaining is just $\int_{h_0}^h \pi r(y)^2 dy = \pi h_1 r^2/3 - \pi h_0 r^2/3 = \pi r^2 (h_1-h_0)/3$.
"""

# ╔═╡ 5a814484-70d4-11ec-2e5c-e54a743c7254
md"""It is not unusual to parameterize a cone by the angle $\theta$ it makes and the height. Since $r/h=\tan\theta$, this gives the formula $V = \pi/3\cdot h^3\tan(\theta)^2$.
"""

# ╔═╡ 5a814498-70d4-11ec-374c-f1aa8ea41eab
md"""##### Example
"""

# ╔═╡ 5a87e410-70d4-11ec-1deb-85d353b8bcbf
md"""[Gabriel's](http://tinyurl.com/8a6ygv) horn is a geometric figure of mathematics - but not the real world - which has infinite height, but not volume! The figure is found by rotating the curve $y=1/x$ around the $x$ axis from $1$ to $\infty$. If the volume formula holds, what is the volume of this "horn?"
"""

# ╔═╡ 5a87f0e0-70d4-11ec-2694-85f9ce344a79
begin
	radius(x) = 1/x
	quadgk(x -> pi*radius(x)^2, 1, Inf)[1]
end

# ╔═╡ 5a87f16c-70d4-11ec-2138-417c4b065543
md"""That is a value very reminiscent of $\pi$, which it is as $\int_1^\infty 1/x^2 dx = -1/x\big|_1^\infty=1$.
"""

# ╔═╡ 5a88bc64-70d4-11ec-26bd-9bed32b448eb
note("""

