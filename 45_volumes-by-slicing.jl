""")

# ╔═╡ 5a88bcfa-70d4-11ec-3a85-150c79432b3a
md"""##### Example
"""

# ╔═╡ 5a88bd68-70d4-11ec-2d2f-15b092c15aee
md"""A movie studio hand is asked to find a prop vase to be used as a [Ming vase](http://en.wikipedia.org/wiki/Chinese_ceramics) in an upcoming scene. The dimensions specified are for the outside diameter in centimeters and are given by
"""

# ╔═╡ 5a88bda6-70d4-11ec-1b28-bfdab99a82af
md"""```math
d(h) = \begin{cases}
2 \sqrt{26^2 - (h-20)^2} & 0 \leq h \leq 44\\
20 \cdot e^{-(h - 44)/10} & 44 < h \leq 50.
\end{cases}
```
"""

# ╔═╡ 5a88bdc2-70d4-11ec-3a9d-e9c46404467a
md"""If the vase were solid, what would be the volume?
"""

# ╔═╡ 5a88be4e-70d4-11ec-2785-27d09cab3e32
md"""We define `d` using a ternary operator to handle the two cases:
"""

# ╔═╡ 5a88dab4-70d4-11ec-0562-7d7aa3ce82ef
begin
	d(h) = h <= 44 ? 2*sqrt(26^2 - (h-20)^2) : 20 * exp(-(h-44)/10)
	rad(h) = d(h)/2
end

# ╔═╡ 5a88db2a-70d4-11ec-37e2-6b2972a4bfc9
md"""The volume in cm$^3$ is then:
"""

# ╔═╡ 5a88e220-70d4-11ec-3332-97896066acb4
Vₜ, _ = quadgk(h -> pi * rad(h)^2, 0, 50)

# ╔═╡ 5a88e252-70d4-11ec-0079-f9889348c4af
md"""For the actual shoot, the vase is to be filled with ash, to simulate a funeral urn. (It will then be knocked over in a humorous manner, of course.) How much ash is needed if the vase has walls that are 1/2 centimeter thick
"""

# ╔═╡ 5a88e28e-70d4-11ec-0f22-430b69a6c8d2
md"""We need to subtract $0.5$ from the radius and `a` then recompute:
"""

# ╔═╡ 5a8900cc-70d4-11ec-17a6-01010d946dc8
V_int, _ = quadgk(h -> pi * (rad(h) - 1/2)^2, 1/2, 50)

# ╔═╡ 5a890124-70d4-11ec-0a59-670a52a1bd13
md"""A liter of volume is $1000 \text{cm}^3$. So this is about $68$ liters, or more than 15 gallons. Perhaps the dimensions given were bit off.
"""

# ╔═╡ 5a890142-70d4-11ec-23d3-355e7832185e
md"""While we are here, to compute the actual volume of the material in the vase could be done by subtraction.
"""

# ╔═╡ 5a890318-70d4-11ec-0099-61fa93307220
Vₜ - V_int

# ╔═╡ 5a890386-70d4-11ec-111c-2f4b5d82d390
md"""### The washer method
"""

# ╔═╡ 5a8903c2-70d4-11ec-14da-478119301b98
md"""Returning to the Michelin Man, in our initial back-of-the-envelope calculation we didn't account for the fact that a tire isn't a disc, as it has its center cut out. Returning, suppose $R_i$ is the outer radius and $r_i$ the inner radius. Then each tire has volume
"""

# ╔═╡ 5a8903ea-70d4-11ec-1c85-f13a8ded61ee
md"""```math
\pi R_i^2 h_i - \pi r_i^2 h_i = \pi (R_i^2 - r_i^2) h_i.
```
"""

# ╔═╡ 5a890412-70d4-11ec-35c4-2b72a5116e0d
md"""Rather than use $\pi r(x)^2$ for a cross section, we would use $\pi (R(x)^2 - r(x)^2)$.
"""

# ╔═╡ 5a891d76-70d4-11ec-332f-b302756d45f1
md"""In general we call a shape like the tire a "washer" and use this formula for a washer's cross section $A_{xc}(x) = \pi(R(x)^2 - r(x)^2)$.
"""

# ╔═╡ 5a891dbe-70d4-11ec-3c0c-5be5068df621
md"""Then the volume for the solid of revolution whose cross sections are washers would be:
"""

# ╔═╡ 5a891dec-70d4-11ec-119d-f99e65bef145
md"""```math
V = \int_a^b \pi \cdot (R(x)^2 - r(x)^2) dx.
```
"""

# ╔═╡ 5a891e16-70d4-11ec-3666-bf98bf7be010
md"""##### Example
"""

# ╔═╡ 5a891e50-70d4-11ec-180f-5548bbcf7631
md"""An artist is working with a half-sphere of material, and wishes to bore out a conical shape. What would be the resulting volume, if the two figures are modeled by
"""

# ╔═╡ 5a891e70-70d4-11ec-102f-fbbd1b948134
md"""```math
R(x) = \sqrt{1^2 - (x-1)^2}, \quad r(x) = x,
```
"""

# ╔═╡ 5a891eb6-70d4-11ec-2431-8baa61f3ba2b
md"""with $x$ ranging from $x=0$ to $1$?
"""

# ╔═╡ 5a891eca-70d4-11ec-36db-b1813968eb3d
md"""The answer comes by integrating:
"""

# ╔═╡ 5a89332e-70d4-11ec-01db-7ff2fb8dc93a
let
	Rad(x) = sqrt(1 - (x-1)^2)
	rad(x) = x
	V, _ = quadgk(x -> pi*(Rad(x)^2 - rad(x)^2), 0, 1)
end

# ╔═╡ 5a89337e-70d4-11ec-1ec5-b5029998610b
md"""## Solids with known cross section
"""

# ╔═╡ 5a893400-70d4-11ec-2b45-67d9d99830bb
md"""The Dart cup company now produces the red solo cup with a [square](http://www.solocup.com/products/squared-plastic-cup/) cross section. Suppose the dimensions are the same: a top diameter of $d_1 = 3 3/4$ inches, a bottom diameter of $d_0 = 2 1/2$ inches and a height of $h = 4 3/4$ inches. What is the volume now?
"""

# ╔═╡ 5a893432-70d4-11ec-1d50-6736693a7662
md"""The difference, of course, is that cross sections now have area $d^2$, as opposed to $\pi r^2$. This leads to some difference, which we quantify, as follows:
"""

# ╔═╡ 5a893d56-70d4-11ec-23bc-1f7375cb1852
let
	d0, d1, h = 2.5, 3.75, 4.75
	d(x) = d0 + (d1 - d0)/h * x
	vol, _ = quadgk(x -> d(x)^2, 0, h)
	vol / 231 * 128
end

# ╔═╡ 5a893d92-70d4-11ec-15e2-937fe48648a1
md"""This shape would have more volume - the cross sections are bigger. Presumably the dimensions have changed. Without going out and buying a cup, let's assume the cross-sectional diameter remained the same, not the diameter. This means the largest dimension is the same. The cross section diameter is $\sqrt{2}$ larger. What would this do to the area?
"""

# ╔═╡ 5a893dce-70d4-11ec-1f93-cb5cc79dd777
md"""We could do this two ways: divide $d_0$ and $d_1$ by $\sqrt{2}$ and recompute. However, each cross section of this narrower cup would simply be $\sqrt{2}^2$ smaller, so the total volume would change by $2$, or be 13 ounces. We have $26.04$ is too big, and $13.02$ is too small, so some other overall dimensions are used.
"""

# ╔═╡ 5a893e0a-70d4-11ec-1e79-8175db1450a3
md"""##### Example
"""

# ╔═╡ 5a893e32-70d4-11ec-0b15-77dab3633b55
md"""For a general cone, we use this [definition](http://en.wikipedia.org/wiki/Cone):
"""

# ╔═╡ 5a893f48-70d4-11ec-105e-09f34e208e23
md"""> A cone is the solid figure bounded by a base in a plane and by a surface (called the lateral surface) formed by the locus of all straight line segments joining the apex to the perimeter of the base.

"""

# ╔═╡ 5a893f72-70d4-11ec-2d8e-233208278594
md"""Let $h$ be the distance from the apex to the base. Consider cones with the property that all planes parallel to the base intersect the cone with the same shape, though perhaps a different scale. This figure shows an example, with the rays coming from the apex defining the volume.
"""

# ╔═╡ 5a894c56-70d4-11ec-2f17-c3dc3f4f3fef
let
	h = 5
	R, r, rho = 1, 1/4, 1/4
	f(t) = (R-r) * cos(t) + rho * cos((R-r)/r * t)
	g(t) = (R-r) * sin(t) - rho * sin((R-r)/r * t)
	ts = range(0, 2pi, length=100)
	
	p = plot(f.(ts), g.(ts), zero.(ts), legend=false)
	for t ∈ range(0, 2pi, length=25)
	    plot!(p, [0,f(t)], [0,g(t)], [h, 0], linecolor=:red)
	end
	p
end

# ╔═╡ 5a894cb0-70d4-11ec-17fe-651da3e91c68
md"""A right circular cone is one where this shape is a circle. This definition can be more general, as a square-based right pyramid is also such a cone. After possibly reorienting the cone in space so the base is at $u=0$ and the apex at $u=h$ the volume of the cone can be found from:
"""

# ╔═╡ 5a894ce2-70d4-11ec-21ae-b17f6f4a65ff
md"""```math
V = \int_0^h A_{xc}(u) du.
```
"""

# ╔═╡ 5a894d1e-70d4-11ec-0fd1-635114017f4d
md"""The cross sectional area $A_{xc}(u)$ satisfies a formula in terms of $A_{xc}(0)$, the area of the base:
"""

# ╔═╡ 5a894d3c-70d4-11ec-0a57-f59b334de963
md"""```math
A_{xc}(u)  = A_{xc}(0) \cdot (1 - \frac{u}{h})^2
```
"""

# ╔═╡ 5a894d50-70d4-11ec-2d89-17068cd19093
md"""So the integral becomes:
"""

# ╔═╡ 5a894d6e-70d4-11ec-23b2-65c03c02bbac
md"""```math
V = \int_0^h A_{xc}(u) du = A_{xc}(0) \int_0^h (1 - \frac{u}{h})^2 du = A_{xc}(0) \int_0^1 v^2 \frac{1}{h} dv = A_{xc}(0) \frac{h}{3}.
```
"""

# ╔═╡ 5a894d82-70d4-11ec-32a8-477aa4bd4dae
md"""This gives a general formula for the volume of such cones.
"""

# ╔═╡ 5a894db4-70d4-11ec-2fe3-35e110bf4f2c
md"""### Cavalieri's method
"""

# ╔═╡ 5a894df0-70d4-11ec-3ee3-7be18aadd86d
md"""[Cavalieri's](http://tinyurl.com/oda9xd9) Principle is "Suppose two regions in three-space (solids) are included between two parallel planes. If every plane parallel to these two planes intersects both regions in cross-sections of equal area, then the two regions have equal volumes." (Wikipedia).
"""

# ╔═╡ 5a894e2a-70d4-11ec-3a8b-817b8ae8a7b1
md"""With the formula for the volume of solids based on cross sections, this is a trivial observation, as the functions giving the cross-sectional area are identical. Still, it can be surprising. Consider a sphere with an interior cylinder bored out of it.  (The [Napkin](http://tinyurl.com/o237v83) ring problem.) The bore has height $h$ - for larger radius spheres this means very wide bores.
"""

# ╔═╡ 5a8965e2-70d4-11ec-3604-336607e92c5c
let
	#The following illustrates $R=5$ and $h=8$.
	
	R =5; h1 = 2*4
	
	theta = asin(h1/2/R)
	thetas = range(-theta, stop=theta, length=100)
	ts = range(-pi, stop=pi, length=100)
	y = h1/4
	
	p = plot(legend=false, aspect_ratio=:equal);
	plot!(p, R*cos.(ts), R*sin.(ts));
	plot!(p, R*cos.(thetas), R*sin.(thetas), color=:orange);
	
	plot!(p, [R*cos.(theta), R*cos.(theta)], [h1/2, -h1/2], color=:orange);
	plot!(p, [R*cos.(theta), sqrt(R^2 - y^2)], [y, y], color=:orange)
	
	plot!(p, [0, R*cos.(theta)], [0,0], color=:red);
	plot!(p,[ 0, R*cos.(theta)], [0,h1/2], color=:red);
	
	annotate!(p, [(.5, -2/3, "sqrt(R²- (h/2)²)"),
	              (R*cos.(theta)-.6, h1/4, "h/2"),
	              (1.5, 1.75*tan.(theta), "R")])
	
	p
end

# ╔═╡ 5a896652-70d4-11ec-3569-a147a8e6bfec
md"""The small orange line is rotated, so using the washer method we get the cross sections given by $\pi(r_0^2 - r_i^2)$, the outer and inner radii, as a function of $y$.
"""

# ╔═╡ 5a89668c-70d4-11ec-210e-819687c5310d
md"""The outer radii has points $(x,y)$ satisfying $x^2 + y^2 = R^2$, so is $\sqrt{R^2 - y^2}$. The inner radii has a constant value, and as indicated in the figure, is $\sqrt{R^2 - (h/2)^2}$, by the Pythagorean theorem.
"""

# ╔═╡ 5a8966aa-70d4-11ec-051d-433499bc06da
md"""Thus the cross sectional area is
"""

# ╔═╡ 5a89674a-70d4-11ec-3344-cf58ab82248a
md"""```math
\pi( (\sqrt{R^2 - y^2})^2 - (\sqrt{R^2 - (h/2)^2})^2 )
= \pi ((R^2 - y^2) - (R^2 - (h/2)^2))
= \pi ((\frac{h}{2})^2 - y^2)
```
"""

# ╔═╡ 5a89677c-70d4-11ec-116e-910a1a90635e
md"""As this does not depend on $R$, and the limits of integration would always be $-h/2$ to $h/2$ by Cavalieri's principle, the volume of the solid will be independent of $R$ too.
"""

# ╔═╡ 5a8967ae-70d4-11ec-0148-b937ad738a17
md"""To actually compute this volume, we take $R=h/2$, so that the bore hole is just a line of no volume, the resulting volume is then that of a sphere with radius $h/2$, or $4/3\pi(h/2)^3 = \pi h^3/6$.
"""

# ╔═╡ 5a8967f4-70d4-11ec-3ae8-5b96d985383e
md"""## The second theorem of Pappus
"""

# ╔═╡ 5a896844-70d4-11ec-1959-b53148e2e052
md"""The second theorem of [Pappus](http://tinyurl.com/l43vw4) says that if a plane figure $F$ is rotated around an axis to form a solid of revolution, the total volume can be written as $2\pi r A(F)$, where $r$ is the distance the centroid is from the axis of revolution, and $A(F)$ is the area of the plane figure. In short, the distance traveled by the centroid times the area.
"""

# ╔═╡ 5a89688a-70d4-11ec-15b1-39e76b40c410
md"""This can make some computations trivial. For example, we can make a torus (or donut) by rotating the circle $(x-2)^2 + y^2 = 1$ about the $y$ axis. As the centroid is clearly $(2, 0)$, with $r=2$ in the above formula, and the area of the circle is $\pi 1^2$, the volume of the donut is $2\pi(2)(\pi) = 4\pi^2$.
"""

# ╔═╡ 5a8968b2-70d4-11ec-002c-87fbe9d11f79
md"""##### Example
"""

# ╔═╡ 5a896916-70d4-11ec-3e7a-c1626f29c713
md"""Above, we found the volume of a cone, as it is a solid of revolution, through the general formula. However, parameterizing the cone as the revolution of a triangle with vertices $(0,0)$, $(r, 0)$, and $(0,h)$ and using the formula for the center of mass in the $x$ direction of such a triangle, $r/3$, we get that the volume of a cone with height $h$ and radius $r$ is  $2\pi (r/3)\cdot (rh/2) = \pi r^2 h/3$, in agreement with the calculus based computation.
"""

