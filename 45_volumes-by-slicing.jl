# ╔═╡ 5a80d710-70d4-11ec-1e6a-036c31171614
md"""If $r(x)$ is the radius as a function of $x$, then the cross sectional area is $\pi r(x)^2$ so the volume is given by:
"""

# ╔═╡ 5a80d72c-70d4-11ec-1d3b-ed5b1cdba6d0
md"""```math
V = \int_a^b \pi r(x)^2 dx.
```
"""

# ╔═╡ 5a80e034-70d4-11ec-2aca-6fe24d823ab1
